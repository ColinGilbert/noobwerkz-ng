pub struct Model {
    pub meshes: TexturedMeshes<TexturedMesh>,
    pub materials: Materials<Material>,
}

impl Model {
    pub fn new() -> Self {
        Self {
            meshes: TexturedMeshes::new(),
            materials: Materials::new(),
        }
    }
}

